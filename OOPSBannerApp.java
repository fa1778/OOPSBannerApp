/**
 * OOPSBannerApp
 * UC3: Print OOPS Banner using String.join()
 */

public class OOPSBannerApp {

    public static void main(String[] args) {

        System.out.println(String.join(" ",
                " *** ", " *** ", "*****", "*****"));

        System.out.println(String.join(" ",
                "*   *", "*   *", "*   *", "*"));

        System.out.println(String.join(" ",
                "*   *", "*   *", "*****", "****"));

        System.out.println(String.join(" ",
                "*   *", "*   *", "*", "*"));

        System.out.println(String.join(" ",
                "*   *", "*   *", "*", "*"));

        System.out.println(String.join(" ",
                "*   *", "*   *", "*", "*"));

        System.out.println(String.join(" ",
                " *** ", " *** ", "*", "*****"));
    }
}